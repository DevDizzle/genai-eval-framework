"""
Output Quality Evaluator
==========================

Uses the **LLM-as-judge** pattern to score generated text on three dimensions:

- **Coherence** (1–5): Logical flow, consistency, structure.
- **Fluency** (1–5): Grammar, readability, natural language quality.
- **Relevance** (1–5): How well the output addresses the prompt.

Supports OpenAI-compatible APIs and can be pointed at local models
(vLLM, Ollama, etc.) by configuring the base URL.

When no API key is available, falls back to heuristic scoring using
sentence-level statistics.
"""

from __future__ import annotations

import json
import os
import re
from typing import Any, Dict, List, Optional

from src.evaluators import BaseEvaluator, EvalResult, Flag, Severity, register

# ---------------------------------------------------------------------------
# Prompts for LLM-as-judge
# ---------------------------------------------------------------------------

JUDGE_SYSTEM_PROMPT = """\
You are an expert text quality evaluator.  You will be given a PROMPT and a
RESPONSE generated by an AI model.  Score the response on three dimensions.

Return ONLY a JSON object with this exact schema — no markdown, no explanation:
{
  "coherence": <int 1-5>,
  "fluency": <int 1-5>,
  "relevance": <int 1-5>,
  "coherence_rationale": "<one sentence>",
  "fluency_rationale": "<one sentence>",
  "relevance_rationale": "<one sentence>"
}

Scoring guide:
- 5: Excellent — publication-ready quality
- 4: Good — minor issues only
- 3: Acceptable — noticeable but not critical issues
- 2: Poor — significant issues affecting usability
- 1: Unacceptable — major problems throughout
"""

JUDGE_USER_TEMPLATE = """\
PROMPT:
{prompt}

RESPONSE:
{response}
"""


# ---------------------------------------------------------------------------
# Heuristic fallback
# ---------------------------------------------------------------------------

_SENT_RE = re.compile(r"(?<=[.!?])\s+(?=[A-Z])")


def _heuristic_quality(text: str, prompt: str = "") -> Dict[str, Any]:
    """Rule-based quality estimation when no LLM API is available.

    This is intentionally simple — the real value is the LLM-as-judge path.
    """
    sentences = _SENT_RE.split(text.strip())
    words = text.split()
    num_words = len(words)
    num_sents = max(len(sentences), 1)

    # Fluency proxy: average sentence length (too short or too long = lower score)
    avg_sent_len = num_words / num_sents
    if 10 <= avg_sent_len <= 25:
        fluency = 4
    elif 5 <= avg_sent_len <= 40:
        fluency = 3
    else:
        fluency = 2

    # Coherence proxy: presence of transition words
    transitions = [
        "however", "therefore", "furthermore", "additionally", "moreover",
        "consequently", "in addition", "as a result", "for example",
        "in contrast", "meanwhile", "specifically", "notably",
    ]
    transition_count = sum(1 for t in transitions if t in text.lower())
    if transition_count >= 3:
        coherence = 4
    elif transition_count >= 1:
        coherence = 3
    else:
        coherence = 2

    # Relevance proxy: word overlap with prompt
    if prompt:
        prompt_words = set(prompt.lower().split())
        response_words = set(text.lower().split())
        overlap = len(prompt_words & response_words) / max(len(prompt_words), 1)
        if overlap > 0.4:
            relevance = 4
        elif overlap > 0.2:
            relevance = 3
        else:
            relevance = 2
    else:
        relevance = 3  # no prompt to compare

    return {
        "coherence": coherence,
        "fluency": fluency,
        "relevance": relevance,
        "coherence_rationale": "Heuristic: based on transition word density.",
        "fluency_rationale": f"Heuristic: avg sentence length = {avg_sent_len:.1f} words.",
        "relevance_rationale": "Heuristic: keyword overlap with prompt."
        if prompt
        else "Heuristic: no prompt provided for comparison.",
        "method": "heuristic",
    }


# ---------------------------------------------------------------------------
# Evaluator
# ---------------------------------------------------------------------------

@register
class QualityEvaluator(BaseEvaluator):
    """LLM-as-judge quality evaluator with heuristic fallback.

    Config keys:
        api_key (str): OpenAI API key (or set OPENAI_API_KEY env var).
        api_base (str): Base URL for OpenAI-compatible API.
        model (str): Judge model name. Default: ``gpt-4o-mini``
        temperature (float): Sampling temperature. Default: 0.0
        use_heuristic (bool): Force heuristic mode. Default: False
    """

    name = "quality"

    def setup(self) -> None:
        """Verify API availability."""
        self._api_key = self.config.get("api_key") or os.getenv("OPENAI_API_KEY")
        self._api_base = self.config.get("api_base", "https://api.openai.com/v1")
        self._model = self.config.get("model", "gpt-4o-mini")
        self._temperature = self.config.get("temperature", 0.0)
        self._force_heuristic = self.config.get("use_heuristic", False)

    # ------------------------------------------------------------------
    # LLM judge call
    # ------------------------------------------------------------------

    def _call_judge(self, prompt: str, response: str) -> Optional[Dict[str, Any]]:
        """Call the LLM judge API.  Returns parsed JSON or None on failure."""
        if self._force_heuristic or not self._api_key:
            return None

        try:
            import openai

            client = openai.OpenAI(api_key=self._api_key, base_url=self._api_base)
            completion = client.chat.completions.create(
                model=self._model,
                temperature=self._temperature,
                messages=[
                    {"role": "system", "content": JUDGE_SYSTEM_PROMPT},
                    {
                        "role": "user",
                        "content": JUDGE_USER_TEMPLATE.format(
                            prompt=prompt, response=response
                        ),
                    },
                ],
                response_format={"type": "json_object"},
            )

            raw = completion.choices[0].message.content
            result = json.loads(raw)
            result["method"] = "llm_judge"
            return result

        except Exception:
            return None

    # ------------------------------------------------------------------
    # Main evaluation
    # ------------------------------------------------------------------

    def evaluate(
        self,
        generated: str,
        source: str = "",
        prompt: str = "",
        **kwargs: Any,
    ) -> EvalResult:
        scores = self._call_judge(prompt, generated)
        if scores is None:
            scores = _heuristic_quality(generated, prompt)

        coherence = int(scores.get("coherence", 3))
        fluency = int(scores.get("fluency", 3))
        relevance = int(scores.get("relevance", 3))

        # Normalise to 0-1 for the composite score
        composite = (coherence + fluency + relevance) / 15.0

        flags: List[Flag] = []
        for dim, val in [("coherence", coherence), ("fluency", fluency), ("relevance", relevance)]:
            if val <= 2:
                flags.append(
                    Flag(
                        category=f"quality:{dim}",
                        message=f"Low {dim} score: {val}/5 — {scores.get(f'{dim}_rationale', '')}",
                        severity=Severity.WARNING,
                    )
                )

        return EvalResult(
            evaluator=self.name,
            score=round(composite, 4),
            sub_scores={
                "coherence": coherence / 5.0,
                "fluency": fluency / 5.0,
                "relevance": relevance / 5.0,
            },
            flags=flags,
            details={
                "coherence": coherence,
                "fluency": fluency,
                "relevance": relevance,
                "coherence_rationale": scores.get("coherence_rationale", ""),
                "fluency_rationale": scores.get("fluency_rationale", ""),
                "relevance_rationale": scores.get("relevance_rationale", ""),
                "method": scores.get("method", "unknown"),
            },
        )
